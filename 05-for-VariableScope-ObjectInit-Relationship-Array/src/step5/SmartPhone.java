package step5;

public class SmartPhone {
	// instance variable (or member variable)
	private String model;
	private int price;
	public String getModel() {
		return model;
	}
	//아래 model은 매개변수 parameter이자 local variable
	public void setModel(String model) {
		this.model = model;
	}
	public int getPrice() {
		return price;
	}
	//price는 parameter 이자 local variable
	public void setPrice(int price) {
		this.price = price;
	}
	
	
}
