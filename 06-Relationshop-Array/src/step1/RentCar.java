package step1;

public class RentCar {
	private String model; //instance variable
	private String color; //instance variable
	
	public RentCar(String model, String color) {
		this.model = model;
		this.color = color;
	}
	
	public String getModel() {
		return model;
	}
	public void setModel(String model) {
		this.model = model;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	
	
}
